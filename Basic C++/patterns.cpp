int main(){
    int n;
    cin >> n;  // 5
    int i = 1;

    while (i<=n){ 

        // 1st triangle
        int j = 1;
        int engine = (n+1) -i; // 5, 4, 3,... // it is reusable in the last while loop

        while (j<= engine){ 
            cout << j;
            j++;
        }

        // 2nd triangle for stars
        int start = 2*i - 2*1;
        while(start){
            cout << "*";
            start--;
        }

        // 3rd triangle, i.e, opposite of 1st one
        while(engine){
            cout << engine;
            engine--;
        }

        cout << "\n";
        i++;
    }
}



     






